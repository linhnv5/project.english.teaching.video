package topica.linhnv5.video.teaching.model;

import java.awt.Color;

/**
 * Config of output video
 * @author ljnk975
 */
public class Config {

	/**
	 * Width of output video
	 */
	private int W;

	/**
	 * Height of output video
	 */
	private int H;

	/**
	 * Opacity of lyric box
	 */
	private float lyricOpacity;

	/**
	 * Size of lyric
	 */
	private float lyricSize;

	/**
	 * Size of lyric Trans
	 */
	private float lyricTransSize;

	/**
	 * Color of lyric
	 */
	private Color lyricColor;

	/**
	 * Color of mark text
	 */
	private Color lyricMarkColor;

	/**
	 * Opacity of word box
	 */
	private float wordBoxOpacity;

	/**
	 * Color of word mark
	 */
	private Color wordBoxPrimaryColor;

	/**
	 * Color of secondary
	 */
	private Color wordBoxSecondaryColor;

	/**
	 * X of wordBox
	 */
	private float wordBoxX;

	/**
	 * Y of wordBox
	 */
	private float wordBoxY;

	/**
	 * Word box duration
	 */
	private float wordBoxTime;

	/**
	 * @return the w
	 */
	public int getW() {
		return W;
	}

	/**
	 * @param w the w to set
	 */
	public void setW(int w) {
		W = w;
	}

	/**
	 * @return the h
	 */
	public int getH() {
		return H;
	}

	/**
	 * @param h the h to set
	 */
	public void setH(int h) {
		H = h;
	}

	/**
	 * @return the lyricOpacity
	 */
	public float getLyricOpacity() {
		return lyricOpacity;
	}

	/**
	 * @param lyricOpacity the lyricOpacity to set
	 */
	public void setLyricOpacity(float lyricOpacity) {
		this.lyricOpacity = lyricOpacity;
	}

	/**
	 * @return the lyricSize
	 */
	public float getLyricSize() {
		return lyricSize;
	}

	/**
	 * @param lyricSize the lyricSize to set
	 */
	public void setLyricSize(float lyricSize) {
		this.lyricSize = lyricSize;
	}

	/**
	 * @return the lyricTransSize
	 */
	public float getLyricTransSize() {
		return lyricTransSize;
	}

	/**
	 * @param lyricTransSize the lyricTransSize to set
	 */
	public void setLyricTransSize(float lyricTransSize) {
		this.lyricTransSize = lyricTransSize;
	}

	/**
	 * @return the lyricColor
	 */
	public Color getLyricColor() {
		return lyricColor;
	}

	/**
	 * @param lyricColor the lyricColor to set
	 */
	public void setLyricColor(Color lyricColor) {
		this.lyricColor = lyricColor;
	}

	/**
	 * @return the lyricMarkColor
	 */
	public Color getLyricMarkColor() {
		return lyricMarkColor;
	}

	/**
	 * @param lyricMarkColor the lyricMarkColor to set
	 */
	public void setLyricMarkColor(Color lyricMarkColor) {
		this.lyricMarkColor = lyricMarkColor;
	}

	/**
	 * @return the wordBoxOpacity
	 */
	public float getWordBoxOpacity() {
		return wordBoxOpacity;
	}

	/**
	 * @param wordBoxOpacity the wordBoxOpacity to set
	 */
	public void setWordBoxOpacity(float wordBoxOpacity) {
		this.wordBoxOpacity = wordBoxOpacity;
	}

	/**
	 * @return the wordBoxPrimaryColor
	 */
	public Color getWordBoxPrimaryColor() {
		return wordBoxPrimaryColor;
	}

	/**
	 * @param wordBoxPrimaryColor the wordBoxPrimaryColor to set
	 */
	public void setWordBoxPrimaryColor(Color wordBoxPrimaryColor) {
		this.wordBoxPrimaryColor = wordBoxPrimaryColor;
	}

	/**
	 * @return the wordBoxSecondaryColor
	 */
	public Color getWordBoxSecondaryColor() {
		return wordBoxSecondaryColor;
	}

	/**
	 * @param wordBoxSecondaryColor the wordBoxSecondaryColor to set
	 */
	public void setWordBoxSecondaryColor(Color wordBoxSecondaryColor) {
		this.wordBoxSecondaryColor = wordBoxSecondaryColor;
	}

	/**
	 * @return the wordBoxX
	 */
	public float getWordBoxX() {
		return wordBoxX;
	}

	/**
	 * @param wordBoxX the wordBoxX to set
	 */
	public void setWordBoxX(float wordBoxX) {
		this.wordBoxX = wordBoxX;
	}

	/**
	 * @return the wordBoxY
	 */
	public float getWordBoxY() {
		return wordBoxY;
	}

	/**
	 * @param wordBoxY the wordBoxY to set
	 */
	public void setWordBoxY(float wordBoxY) {
		this.wordBoxY = wordBoxY;
	}

	/**
	 * @return the wordBoxTime
	 */
	public float getWordBoxTime() {
		return wordBoxTime;
	}

	/**
	 * @param wordBoxTime the wordBoxTime to set
	 */
	public void setWordBoxTime(float wordBoxTime) {
		this.wordBoxTime = wordBoxTime;
	}

}
